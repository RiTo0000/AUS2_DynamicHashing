    private void jButtonLandDetailEditLandActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonLandDetailEditLandActionPerformed
        int row = this.jTableLands.getSelectedRow();
        
        LandDH selectedLand = this.lands.get(row);
        
        boolean areaEdited = false;
        //nacitam si vsetky polia a skontrolujem aky typ editacie mi treba robit
        int landNum = Integer.parseInt(this.jTextFieldLandDetailLandNumVal.getText());
        String description = this.jTextFieldLandDetailDescVal.getText();
        //suradnice polohy
        Direction startXDir = Direction.getDirectFromString(this.jComboBoxLandDetailStartXDirect.getSelectedItem().toString());
        double startXVal = Double.parseDouble(this.jTextFieldLandDetailStartXVal.getText());
        Direction startYDir = Direction.getDirectFromString(this.jComboBoxLandDetailStartYDirect.getSelectedItem().toString());
        double startYVal = Double.parseDouble(this.jTextFieldLandDetailStartYVal.getText());
        Direction endXDir = Direction.getDirectFromString(this.jComboBoxLandDetailEndXDirect.getSelectedItem().toString());
        double endXVal = Double.parseDouble(this.jTextFieldLandDetailEndXVal.getText());
        Direction endYDir = Direction.getDirectFromString(this.jComboBoxLandDetailEndYDirect.getSelectedItem().toString());
        double endYVal = Double.parseDouble(this.jTextFieldLandDetailEndYVal.getText());
        
        
        Area newLandSpace = new Area(new Point(new Coordinate(startXDir, startXVal), new Coordinate(startYDir, startYVal)), 
                                        new Point(new Coordinate(endXDir, endXVal), new Coordinate(endYDir, endYVal)));
        if (!selectedLand.getSpace().equals(newLandSpace)) 
            areaEdited = true;
        
        if (areaEdited) { //nastala zmena v polohe parcely musim spravit hlbsi edit (uprava v QuadStrome)
            try {
                this.application.editLand(selectedLand, newLandSpace, landNum, description);
            } catch (Exception ex) {
                Logger.getLogger(AppUI.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        else if (selectedLand.getLandNumber() == landNum &&
                selectedLand.getDescription().equals(description)) {
            JOptionPane.showMessageDialog(this.jPanelLandDetail, "Nič sa nezmenilo, editácia nie je potrebná");
            return; //nenastala ziadna zmena
        }
        else { // nastala zmena iba v jednom z 2 neklucovych poli menim iba tie
            selectedLand.setLandNumber(landNum);
            selectedLand.setDescription(description);
        }
        
        // obnova zobrazenia aktualnych parciel
        this.printLandsTable(); 
        this.jDialogLandDetail.setVisible(false);
        JOptionPane.showMessageDialog(this.jPanelSearchLands, "Editácia parcely prebehla úspešne");
    }//GEN-LAST:event_jButtonLandDetailEditLandActionPerformed

