    private void printPropertiesTable() {
        String [] properties_out;
            
        DefaultTableModel model = (DefaultTableModel)this.jTableProperties.getModel();
        model.setRowCount(0); //odstranenie existujucich riadkov
        for (int i = 0; i < this.properties.size(); i++) {
            properties_out = this.properties.get(i).getInfo();
            model.addRow(properties_out);
        }

        this.jButtonDeleteProperty.setEnabled(false);
        this.jButtonEditProperty.setEnabled(false);
        this.jButtonShowPropertyDetail.setEnabled(false);
    }
    
