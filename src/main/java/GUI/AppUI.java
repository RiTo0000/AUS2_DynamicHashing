    private void jButtonInitializeAppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInitializeAppActionPerformed
        
        double startXVal = Double.parseDouble(this.jTextFieldStartXVal.getText());
        double startYVal = Double.parseDouble(this.jTextFieldStartYVal.getText());
        double endXVal = Double.parseDouble(this.jTextFieldEndXVal.getText());
        double endYVal = Double.parseDouble(this.jTextFieldEndYVal.getText());
        
        Direction startXDir = Direction.getDirectFromString(this.jComboBoxStartXDirect.getSelectedItem().toString());
        Direction startYDir = Direction.getDirectFromString(this.jComboBoxStartYDirect.getSelectedItem().toString());
        Direction endXDir = Direction.getDirectFromString(this.jComboBoxEndXDirect.getSelectedItem().toString());
        Direction endYDir = Direction.getDirectFromString(this.jComboBoxEndYDirect.getSelectedItem().toString());
        
        this.application = new App(new Area(new Point(new Coordinate(startXDir, startXVal), new Coordinate(startYDir, startYVal)), 
                                            new Point(new Coordinate(endXDir, endXVal), new Coordinate(endYDir, endYVal))));
        
        JOptionPane.showMessageDialog(this.jPanelInitApp, "Aplikácia bola inicializovaná");
    }//GEN-LAST:event_jButtonInitializeAppActionPerformed

    private void jButtonSearchPropertiesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSearchPropertiesActionPerformed
        PropertyDH foundProperty = null;
        
        
        if (this.application == null) {
            JOptionPane.showMessageDialog(this.jPanelSearchProperties, "Aplikácia nie je inicializovaná", "Chyba", JOptionPane.ERROR_MESSAGE);
        }
        else {
            if (this.jTextFieldSearchPropIDVal.getText().isEmpty()) {
                try {
                    this.properties = this.application.getProperties();
                } catch (IOException ex) {
                    Logger.getLogger(AppUI.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
            else {
                int propId = Integer.parseInt(this.jTextFieldSearchPropIDVal.getText());
                if (propId > 0) {
                    try {
                        foundProperty = this.application.getProperty(propId);
                    } catch (Exception ex) {
                        Logger.getLogger(AppUI.class.getName()).log(Level.SEVERE, null, ex);
                    } 
                    this.properties.clear(); 
                    if (foundProperty != null) {
                        this.properties.add(foundProperty);
                    }
                }
                else {
                    //TODO zla konverzia
                }
            }
            
            this.printPropertiesTable();            
        }
        
        
        
    }//GEN-LAST:event_jButtonSearchPropertiesActionPerformed

